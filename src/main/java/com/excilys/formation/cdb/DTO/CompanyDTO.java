package com.excilys.formation.cdb.DTO;

/**
 * Classe représentant une company avec ses différentes valeurs.
 * @author kylian
 *
 */
public class CompanyDTO {

	private int id;

	private String name;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
